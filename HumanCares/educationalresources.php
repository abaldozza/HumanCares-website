                <p>Since the Industrial Revolution, human activity has led to the emission of a considerable amount of greenhouse gases into the atmosphere. Greenhouse gases such as carbon dioxide, nitrous oxide, and methane have been blamed for the global warming phenomenon, an essential aspect of climate change.</p>
                <a href="https://www.epa.gov/climate-indicators" target="_blank" class="btn">learn more <span class="fas fa-chevron-right"></span></a>
