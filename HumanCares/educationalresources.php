                <h3>Evidence of climate change</h3>
                <p>Scientific information taken from natural sources (such as ice cores, rocks, and tree rings) and from modern equipment (like satellites and instruments) all show the signs of a changing climate.</p>
                <a href="https://climate.nasa.gov/evidence/" target="_blank" class="btn">learn more <span class="fas fa-chevron-right"></span></a>
